package Unidad2;

public class Ejemplo1Arrays 
{

	public static void main(String[] args) 
	{
		int [] arrayEjemplo = {2,5,1,3,4};
		
		int i = 0;
		
		while (i < arrayEjemplo.length)
		{
			System.out.print(arrayEjemplo[i] + " ");
			i++;
		}

	}

}

