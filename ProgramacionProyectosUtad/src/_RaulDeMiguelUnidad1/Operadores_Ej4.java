package _RaulDeMiguelUnidad1;

public class Operadores_Ej4 
{

	public static void main(String[] args) 
	{
		byte num1 = 4;
		byte num2 = 2;
		
		System.out.println ("4+2=" + (num1 + num2));
		System.out.println ("4-2=" + (num1 - num2));
		System.out.println ("4*2=" + (num1 * num2));
		System.out.println ("4/2=" + (num1 / num2));

	}

}

/* En este ejercicio nos piden declarar dos variables de tipo byte,
 * para después enviar por pantalla diferentes operaciones con estas dos variables.
 * 
 * Para ello, simplemente las declaramos como tipo de dato byte y después utilizamos println para que,
 * en la salida, se muestre su operación (suma, resta, multiplicación y división), seguido de un salto de línea
 * en cada uno de los resultados de las operaciones realizadas. */

